package Unit7;

public class Employee {
    private String name;
    private int salary;

    public String getName()
    {
        return name;
    }
    public void setName(String mName)
    {
        name = mName;
    }
    public int getSalary()
    {
        return salary;
    }
    public void setSalary(int mSalary)
    {
        salary = mSalary;
    }
}
