package Unit7;

public class ReadingMaterial {
    int cost;
    int pages;
    int wordsPerPage;

    public int getCost()
    {
        return cost;
    }

    public void setCost(int mCost) {
        cost = mCost;
    }

    public int getPages()
    {
        return pages;
    }

    public void setPages(int mPages)
    {
        pages = mPages;
    }

    public int setWordsPerPage()
    {
        return wordsPerPage;
    }

    public void setWordsPerPage(int mWordsPerPage) {
        wordsPerPage = mWordsPerPage;
    }
}
